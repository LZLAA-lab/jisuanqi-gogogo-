#include<iostream>
#include<cmath>
using namespace std;
double add(double a, double b)//加法运算
{
	double sum = a + b;
	return sum;
}
double sub(double a, double b)//减法运算
{
	double diff = a - b;
	return diff;
}
double mul(double a, double b)//乘法运算
{
	double pro = a * b;
	return pro;
}
double chu(double a, double b)//除法运算
{
	double quo = a / b;
	return quo;
}